    //Encryption method
    private Editable AES_Encryption(Editable editable) {
        String string = editable.toString();
        byte[] byteString = string.getBytes();
        byte[] encryptedByte = new byte[byteString.length];

        try {
            cipher.init(Cipher.ENCRYPT_MODE, secretKeySpec);
            encryptedByte = cipher.doFinal(byteString);
        } catch (InvalidKeyException e) {
            e.printStackTrace();
        } catch (BadPaddingException e) {
            e.printStackTrace();
        } catch (IllegalBlockSizeException e) {
            e.printStackTrace();
        }

        String returnString = null;
        try {
            returnString = new String(encryptedByte, "ISO-8859-1");
        } catch (UnsupportedEncodingException e) {
            e.printStackTrace();
        }

        Editable finished = new SpannableStringBuilder(returnString);
        return finished;
    }

    //Decryption method
    private String AES_Decryption(String string) throws UnsupportedEncodingException {
        //String string = encryptedEditable.toString();
        byte[] encryptedByte = string.getBytes("ISO-8859-1");
        String decryptedString = string;

        byte[] decryption;

        try {
            decipher.init(Cipher.DECRYPT_MODE, secretKeySpec);
            decryption = decipher.doFinal(encryptedByte);
            decryptedString = new String(decryption);
        } catch (InvalidKeyException e) {
            e.printStackTrace();
        } catch (BadPaddingException e) {
            e.printStackTrace();
        } catch (IllegalBlockSizeException e) {
            e.printStackTrace();
        }
        //Editable finishedDecrypt = new SpannableStringBuilder(decryptedString);
        return decryptedString;
    }
